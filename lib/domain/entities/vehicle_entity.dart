class VehicleEntity {
  final String id;
  final String brand;
  final String model;
  final String type;
  final String fuelType;
  final String registrationNumber;
  final String userId;

  const VehicleEntity({
    required this.id,
    required this.brand,
    required this.model,
    required this.type,
    required this.fuelType,
    required this.registrationNumber,
    required this.userId,
  });
}
